# 전 규칙 통합 + 일본어 길이 내림차순(긴 것 먼저 치환)
_ALL = list(REACT.items()) + REPL_ITEMS + EXTRA
_ALL = sorted(_ALL, key=lambda kv: -len(kv[0]))

def translate(line):
    """line: 일본어 대사. 부분치환으로 한국어화(앞뒤 ！·\\n 등 구조 보존)."""
    out = line
    for jp, kr in _ALL:
        if jp in out:
            out = out.replace(jp, kr)
    return out

def has_jp(s):
    return any('\u3040'<=c<='\u30ff' or '\u4e00'<=c<='\u9fff' for c in s)
